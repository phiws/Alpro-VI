# Assignment 

# Penjumlahan -> "+="
A1 = 3
A1 += 3  # A1 = A1 + 3
print(A1)

# Pengurangan -> "-="
A2= 2 
A2 -= 2 # A2 = A2 - 2
print(A2)

# Perkalian -> "*="
A3 = 2
A3 *= 3  #  = A3 * 3
print (A3)

# Pembagian -> "/="
A4 = 4
A4 /= 5  # A4 = A4 / 4
print(A4)

# Pangkat -> "**="
A5 = 5
A5 **= 2  # A5 = A5 ** 2
print(A5)

# Pembulatan bawah -> "//="
A6 = 4
A6 //= 2  # A6 = A6 // 2
print(A6)

# Modulus -> "%="
a = 2
a *= 3 # -> a = a * 3
print (a)