# Operator perbandingan (minimal 1)
    # hasil -> boolean

# Lebih Besar -> ">"
a = 2
b = 3
perbandingan = a > b
print("perbandingan a > b:", perbandingan)

# Lebih Besar Sama Dengan -> ">="
a = 2
b = 2
perbandingan = a >= b
print("perbandingan a > b:", perbandingan)

# Lebih Kecil -> "<"
a = 2
b = 3
perbandingan = a < b
print("Perbandingan a < b:", perbandingan)

# Lebih Kecil Sama Dengan -> "<="
a = 2
b = 2
perbandingan = a <= b
print("Perbandingan a <= b:", perbandingan)

# Sama Dengan -> "=="
a = 2
b = 2
perbandingan = a == b
print("Perbandingan a == b:", perbandingan)

# Tidak Sama Dengan -> "!="
a = 2
b = 1
perbandingan = a != b
print("perbandingan a != b:", perbandingan)